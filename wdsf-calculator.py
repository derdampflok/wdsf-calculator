def determine_number_of_couples_modifier(n):

    if n < 7:
        return -30
    if n < 12:
        return -20
    if n < 24:
        return -10
    if n < 36:
        return 0
    if n < 48:
        return 10
    if n < 96:
        return 25
    if n < 144:
        return 30
    if n < 192:
        return 35
    if n < 288:
        return 40
    return 50


def determine_top_12_modifier(n):
    if n > 9:
        return 70
    if n > 6:
        return 50
    return 0


def determine_top_24_modifier(n):
    if n > 9:
        return 35
    if n > 6:
        return 25
    return 0


def determine_top_36_modifier(n):
    if n > 6:
        return 10
    return 0


def determine_countries_modifier(n):
    if n > 23:
        return 50
    if n > 15:
        return 40
    if n > 10:
        return 25
    if n > 7:
        return 10
    return 0


